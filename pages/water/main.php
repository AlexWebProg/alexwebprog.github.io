            <h3 class="section-heading header">Живая вода</h3>
            <p class="text-justify">
                Пожалуй, наиболее полно процесс очистки водопроводной воды описывает Вадим Зеланд в книге "Апокрифический трансерфинг".
                Его метод состоит в следующем:
                <br/>
                1. Вода полностью очищается при помощи дистиллятора или фильтра обратного осмоса;
                <br/>
                2. Вода настаивается на шунгите и кремние. Также можно добавить горный кварц;
                <br/>
                3. Вода активируется прибором «Живая и мертвая вода» (электроактиватором);
                <br/>
                4. Вода структурируется при помощи аквадиска или корректора функционального состояния Кольцова (КФС).
                <br/>
                Возможен и другой, упрощённый способ:
                <br/>
                1. Вода максимально очищается при помощи доступного Вам фильтра;
                <br/>
                2. Вода настаивается на шунгите и/или кремние. Желательно при этом не ставить воду в тёмное место, а держать на свету.
                После этого нижние слой воды в 2-3 сантиметра необходимо вылить, так как в нём осаживается всё вредное;
                <br/>
                3. Вода замораживается в морозилке, но не полностью, а до тех пор, пока не останется небольшая незамёрзшая часть воды.
                Её снова нужно слить, а затем разморозить воду и наслаждаться её чудесным вкусом. Желательно такую воду выпивать
                в течение первых 7 часов после разморозки.
